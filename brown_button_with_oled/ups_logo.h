  oled.println("N") ;

  oled.setCursor(110, 8*2);
  oled.println("B") ;
  oled.setCursor(110, 8*3);
  oled.println("U") ;
  oled.setCursor(110, 8*4);
  oled.println("T") ;
  oled.setCursor(110, 8*5);
  oled.println("T") ;
  oled.setCursor(110, 8*6);
  oled.println("O") ;
  oled.setCursor(110, 8*7);
